int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] < 65 || *str > 122 || (*str > 90 && *str < 97))
			return (0);
		i++;
	}
	return (1);
}
