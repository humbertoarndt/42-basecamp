int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb <= 0)
		return (0);
	while (i * i < nb)
	{
		i++;
		if (i == 46340)
			break ;
	}
	if (i * i == nb)
		return (i);
	return (0);
}
